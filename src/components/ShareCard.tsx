"use client";

import { forwardRef } from "react";
import Image from "next/image";
import type { Game, ChatMessage } from "./types";
import { moneyline } from "./utils";

interface ShareCardProps {
  mode: "daily" | "chat";
  headline: string;
  dailyMarkdown: string;
  selectedGame: Game | null;
  chatMessages: ChatMessage[];
}

const ShareCard = forwardRef<HTMLDivElement, ShareCardProps>(function ShareCard(
  { mode, headline, dailyMarkdown, selectedGame, chatMessages },
  ref,
) {
  return (
    <div
      aria-hidden="true"
      ref={ref}
      className="fixed left-[-9999px] top-[-9999px] z-0 w-[760px] bg-[#0a0e1a] p-6 text-sm"
    >
      <div className="flex items-center justify-between border-b border-white/15 pb-4">
        <Image src="/lockin-logo.png" alt="LOCKIN" width={130} height={40} />
        <div className="mono text-xs text-[#8b92a5]">lockinpicks.ai</div>
      </div>
      {mode === "daily" ? (
        <div className="mt-4">
          <div className="heading mb-2 text-xl text-[#00ff87]">LOCKIN Daily Edge</div>
          <div className="text-[#8b92a5]">{headline}</div>
          <p className="mt-3 whitespace-pre-wrap text-[#f5f5f3]">{dailyMarkdown}</p>
        </div>
      ) : (
        <div className="mt-4">
          <div className="heading mb-2 text-xl text-[#00ff87]">
            {selectedGame
              ? `${selectedGame.awayTeam} @ ${selectedGame.homeTeam} Insight`
              : "LOCKIN Match Insight"}
          </div>
          <div className="text-[#8b92a5]">
            {selectedGame
              ? `${selectedGame.awayTeam} ML ${moneyline(selectedGame.awayMoneyline)} / ${selectedGame.homeTeam} ML ${moneyline(selectedGame.homeMoneyline)}`
              : ""}
          </div>
          <div className="mt-3 whitespace-pre-wrap text-[#f5f5f3]">
            {chatMessages
              .filter((item) => item.role === "assistant")
              .slice(-1)
              .map((msg) => msg.content)
              .join("\n") || "No chat summary yet."}
          </div>
        </div>
      )}
      <div className="mt-5 border-t border-white/10 pt-3 text-[12px] text-[#8b92a5]">
        Generated by LOCKIN &middot; AI-powered NBA analysis
      </div>
    </div>
  );
});

export default ShareCard;
